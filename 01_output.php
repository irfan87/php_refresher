<?php
// echo - Output strings, numbers, html, etc
echo 123, "name";

// print() - work like echo, but can only take in a single argument
print("hello world");

// print_r() = print single values and arrays
print_r([1, 2, 3, 4]);

// var_dump - returns more info like data types and length
var_dump('Hello');

// var_export() - similar to var_dump(). Outputs a string representation of variables
var_export("hello");